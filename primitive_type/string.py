#str AKA string
a:str = 'Hello Guys'
print(a)

b:str = "welcome"

print(a+b) #no space
print('single column')
print(a+''+b) #no space in '' results no space in strings. hiworld
print(a+' '+b) #space in ' ' results in space between strings. hi world

print("double column")
print(a+""+b) #no space in "" results no space in strings. hiworld
print(a+" "+b) #space in " " results in space between strings. hi world